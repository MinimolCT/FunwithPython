# program to show different usages of range() function
print (' Range function with only one argument: end')
print('---------------------------------------')
for i in range(11):
    print(i)
print (' Range function with two argument : start and end')
print('---------------------------------------------------')
for i in range (2,11):
    print(i)
print (' Range function with two argument : start , end and increment')
print('--------------------------------------------------------------')
for i in range(3, 30 ,3):
    print (i)
